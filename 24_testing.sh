#!/usr/bin/bash

mkdir -p exports

set -e

echo -e "\033[1;92m
######################################################################################################################################
####### Copy tarballs
######################################################################################################################################\033[0m"
sudo mkdir -p /runner/project
sudo cp -r collections_tarballs /runner/project


echo -e "\033[1;92m
######################################################################################################################################
####### APPLY: Null
######################################################################################################################################\033[0m"
ansible-playbook configure_aap.yml -e aap_version=24 -e object_set=null --vault-password-file ansible_vault > exports/export_24_Null.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### VERIFY: Null
######################################################################################################################################\033[0m"
/bin/cat exports/export_24_Null.txt | grep changed


echo -e "\033[1;92m
######################################################################################################################################
####### APPLY: Empty
######################################################################################################################################\033[0m"
ansible-playbook configure_aap.yml -e aap_version=24 -e object_set=empty -e delete_objects=true --vault-password-file ansible_vault


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Empty
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml > exports/export_24_empty.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Empty | AWX
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml -e aap_platform=awx24 > exports/export_24_empty_awx.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Empty | 25
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml -e format_for_25=true > exports/export_24_empty_25.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Empty | AWX/25
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml -e aap_platform=awx24 -e format_for_25=true > exports/export_24_empty_awx_25.txt 2>&1


set +e

echo -e "\033[1;92m
######################################################################################################################################
####### COMPARE: export EMPTY 24 to EMPTY AWX
######################################################################################################################################\033[0m"
diff exports/export_24_empty.txt exports/export_24_empty_awx.txt


echo -e "\033[1;92m
######################################################################################################################################
####### COMPARE: export EMPTY 25 to EMPTY AWX 25
######################################################################################################################################\033[0m"
diff exports/export_24_empty_25.txt exports/export_24_empty_awx_25.txt

set -e

echo -e "\033[1;92m
######################################################################################################################################
####### CHECK: Set A
######################################################################################################################################\033[0m"
ansible-playbook configure_aap.yml -e aap_version=24 -e object_set=A --vault-password-file ansible_vault --check


echo -e "\033[1;92m
######################################################################################################################################
####### APPLY: Set A
######################################################################################################################################\033[0m"
ansible-playbook configure_aap.yml -e aap_version=24 -e object_set=A -e delete_objects=true -e wait_project_sync=true -e trigger_inventory_sync=true --vault-password-file ansible_vault


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Set A
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml > exports/export_24_A.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Set A | AWX
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml -e aap_platform=awx24 > exports/export_24_A_awx.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Set A | 25
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml -e format_for_25=true > exports/export_24_A_25.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Set A | AWX/25
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml -e aap_platform=awx24 -e format_for_25=true > exports/export_24_A_awx_25.txt 2>&1

set +e

echo -e "\033[1;92m
######################################################################################################################################
####### COMPARE: export A to A AWX
######################################################################################################################################\033[0m"
diff exports/export_24_A.txt exports/export_24_A_awx.txt


echo -e "\033[1;92m
######################################################################################################################################
####### COMPARE: export A 25 to A AWX 25
######################################################################################################################################\033[0m"
diff exports/export_24_A_25.txt exports/export_24_A_awx_25.txt

set -e

echo -e "\033[1;92m
######################################################################################################################################
####### APPLY: Set B | ORG limit
######################################################################################################################################\033[0m"
ansible-playbook configure_aap.yml -e aap_version=24 -e object_set=B --tags controller_config -e "{'limit_organizations':['Org D','Org E']}" -e delete_objects=true -e wait_project_sync=true --vault-password-file ansible_vault > exports/export_24_ORG_LIMIT.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### VERIFY: Set B | Org limit
######################################################################################################################################\033[0m"
/bin/cat exports/export_24_ORG_LIMIT.txt | grep changed | grep -Ev 'D1|D2|E1|E2'


echo -e "\033[1;92m
######################################################################################################################################
####### CHECK: Set B
######################################################################################################################################\033[0m"
ansible-playbook configure_aap.yml -e aap_version=24 -e object_set=B --vault-password-file ansible_vault --check


echo -e "\033[1;92m
######################################################################################################################################
####### APPLY: Set B
######################################################################################################################################\033[0m"
ansible-playbook configure_aap.yml -e aap_version=24 -e object_set=B -e delete_objects=true -e wait_project_sync=true --vault-password-file ansible_vault


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Set B
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml > exports/export_24_B.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Set B | AWX
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml -e aap_platform=awx24 > exports/export_24_B_awx.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Set B | 25
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml -e format_for_25=true > exports/export_24_B_25.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Set B | AWX/25
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml -e aap_platform=awx24 -e format_for_25=true > exports/export_24_B_awx_25.txt 2>&1

set +e

echo -e "\033[1;92m
######################################################################################################################################
####### COMPARE: export B to B AWX
######################################################################################################################################\033[0m"
diff exports/export_24_B.txt exports/export_24_B_awx.txt


echo -e "\033[1;92m
######################################################################################################################################
####### COMPARE: export B 25 to B AWX 25
######################################################################################################################################\033[0m"
diff exports/export_24_B_25.txt exports/export_24_B_awx_25.txt

set -e

echo -e "\033[1;92m
######################################################################################################################################
####### CHECK: Set B (idempotency)
######################################################################################################################################\033[0m"
ansible-playbook configure_aap.yml -e aap_version=24 -e object_set=B --vault-password-file ansible_vault --check > exports/export_24_B_idempotency_check.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### APPLY: Set B (idempotency)
######################################################################################################################################\033[0m"
ansible-playbook configure_aap.yml -e aap_version=24 -e object_set=B --vault-password-file ansible_vault >> exports/export_24_B_idempotency_check.txt 2>&1


echo -e "\033[1;92m
######################################################################################################################################
####### VERIFY: Set B (idempotency)
######################################################################################################################################\033[0m"
/bin/cat exports/export_24_B_idempotency_check.txt | grep changed


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Set B (settings, auth, users,roles)
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml --tags export_settings,export_authenticators,export_users,export_roles


echo -e "\033[1;92m
######################################################################################################################################
####### GET: Set B (settings, auth, users,roles)/25
######################################################################################################################################\033[0m"
ansible-playbook get_objects.yml --tags export_settings,export_authenticators,export_users,export_roles -e format_for_25=true


